Get-Help

Get-Help *command*
...the asterix acts as a wildcard...meaning, find any cmdlet which contains either the exact name.../
or, with other word prefixes written either before/or, after.

Get-Command

Get-Alias

...all of the above are common ways of finding Powershell help.

-----

Yet, another way of finding help with Powershell commands is to open up the ISE/Integrated Script Editor...; 
which when you type in commands...it, automatically, uses intellisense to complete the text.
So, if you were to type in 'a'...you will see results such as: add/active/-etc.
